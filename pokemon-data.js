// 获取随机宝可梦(从池子中)
function getRandomPokemon(mbtiType) {
    const pool = pokemonPools[mbtiType];
    if (!pool || !pool.pokemons || pool.pokemons.length === 0) {
        return null;
    }
    
    const randomIndex = Math.floor(Math.random() * pool.pokemons.length);
    return pool.pokemons[randomIndex];
}

